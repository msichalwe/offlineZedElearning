                    ].divide(const SizedBox(height: 4.0)),
                  ).animateOnPageLoad(
                      animationsMap['columnOnPageLoadAnimation']!),
                ),
              ),
            ].addToEnd(const SizedBox(height: 56.0)),
          ),
        ),
      ),
    );
